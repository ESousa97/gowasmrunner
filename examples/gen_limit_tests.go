//go:build ignore

package main

import (
	"log"
	"os"
)

func main() {
	// 1. Infinite Loop Wasm (to test Timeout)
	// (module (func (export "infinite_loop") (loop (br 0))))
	infiniteLoopWasm := []byte{
		0x00, 0x61, 0x73, 0x6d, 0x01, 0x00, 0x00, 0x00,
		0x01, 0x04, 0x01, 0x60, 0x00, 0x00,
		0x03, 0x02, 0x01, 0x00,
		0x07, 0x11, 0x01, 0x0d, 0x69, 0x6e, 0x66, 0x69, 0x6e, 0x69, 0x74, 0x65, 0x5f, 0x6c, 0x6f, 0x6f, 0x70, 0x00, 0x00,
		0x0a, 0x06, 0x01, 0x04, 0x00, 0x03, 0x40, 0x0c, 0x00, 0x0b, 0x0b,
	}
	os.WriteFile("examples/infinite_loop.wasm", infiniteLoopWasm, 0644)

	// 2. Memory Allocation Wasm (to test Limit)
	// (module (memory (export "memory") 100)) ;; Tries to allocate 100 pages (6.4MB)
	// Our runner limits to 10 pages (640KB).
	memoryLimitWasm := []byte{
		0x00, 0x61, 0x73, 0x6d, 0x01, 0x00, 0x00, 0x00,
		0x05, 0x03, 0x01, 0x00, 0x64, // Memory section: 1 memory, min 100 pages
		0x07, 0x0a, 0x01, 0x06, 0x6d, 0x65, 0x6d, 0x6f, 0x72, 0x79, 0x02, 0x00,
	}
	os.WriteFile("examples/memory_limit.wasm", memoryLimitWasm, 0644)

	log.Println("Created infinite_loop.wasm and memory_limit.wasm")
}
