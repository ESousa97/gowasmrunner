package main

import (
	"log"
	"os"
)

// Generates a Wasm binary that exports memory, allocate, and greet functions.
// The greet function takes a string pointer+length and returns "Hello, <name>".
//
// WAT representation:
//
//	(module
//	  (memory (export "memory") 1)
//
//	  (func (export "allocate") (param i32) (result i32)
//	    (i32.const 1024)
//	  )
//
//	  (func (export "greet") (param $ptr i32) (param $len i32) (result i64)
//	    (local $i i32)
//
//	    ;; Write "Hello, " (7 bytes) at offset 2048
//	    (i32.store8 (i32.const 2048) (i32.const 72))   ;; 'H'
//	    (i32.store8 (i32.const 2049) (i32.const 101))  ;; 'e'
//	    (i32.store8 (i32.const 2050) (i32.const 108))  ;; 'l'
//	    (i32.store8 (i32.const 2051) (i32.const 108))  ;; 'l'
//	    (i32.store8 (i32.const 2052) (i32.const 111))  ;; 'o'
//	    (i32.store8 (i32.const 2053) (i32.const 44))   ;; ','
//	    (i32.store8 (i32.const 2054) (i32.const 32))   ;; ' '
//
//	    ;; Copy input string to offset 2055
//	    (loop $copy
//	      (block $exit
//	        (br_if $exit (i32.eq (local.get $i) (local.get $len)))
//	        (i32.store8
//	          (i32.add (i32.const 2055) (local.get $i))
//	          (i32.load8_u (i32.add (local.get $ptr) (local.get $i)))
//	        )
//	        (local.set $i (i32.add (local.get $i) (i32.const 1)))
//	        (br $copy)
//	      )
//	    )
//
//	    ;; Return (offset=2048 << 32 | len=7+$len)
//	    (i64.or
//	      (i64.shl (i64.extend_i32_u (i32.const 2048)) (i64.const 32))
//	      (i64.extend_i32_u (i32.add (i32.const 7) (local.get $len)))
//	    )
//	  )
//	)
func main() {
	// Bytecode identical to the original gen_greet.go proven to work
	wasmBinary := []byte{
		0x00, 0x61, 0x73, 0x6d, 0x01, 0x00, 0x00, 0x00, // Header
		0x01, 0x0c, 0x02, 0x60, 0x01, 0x7f, 0x01, 0x7f, 0x60, 0x02, 0x7f, 0x7f, 0x01, 0x7e, // Types
		0x03, 0x03, 0x02, 0x00, 0x01, // Functions
		0x05, 0x03, 0x01, 0x00, 0x01, // Memory
		0x07, 0x1d, 0x03, 0x06, 0x6d, 0x65, 0x6d, 0x6f, 0x72, 0x79, 0x02, 0x00, 0x08, 0x61, 0x6c, 0x6c, 0x6f, 0x63, 0x61, 0x74, 0x65, 0x00, 0x00, 0x05, 0x67, 0x72, 0x65, 0x65, 0x74, 0x00, 0x01, // Exports
		0x0a, 0x7e, 0x02, 0x05, 0x00, 0x41, 0x80, 0x08, 0x0b, 0x76, 0x01, 0x01, 0x7f, 0x41, 0x80, 0x10, 0x41, 0xc8, 0x00, 0x3a, 0x00, 0x00, 0x41, 0x81, 0x10, 0x41, 0xe5, 0x00, 0x3a, 0x00, 0x00, 0x41, 0x82, 0x10, 0x41, 0xec, 0x00, 0x3a, 0x00, 0x00, 0x41, 0x83, 0x10, 0x41, 0xec, 0x00, 0x3a, 0x00, 0x00, 0x41, 0x84, 0x10, 0x41, 0xef, 0x00, 0x3a, 0x00, 0x00, 0x41, 0x85, 0x10, 0x41, 0x2c, 0x3a, 0x00, 0x00, 0x41, 0x86, 0x10, 0x41, 0x20, 0x3a, 0x00, 0x00, 0x03, 0x40, 0x02, 0x40, 0x20, 0x02, 0x20, 0x01, 0x46, 0x0d, 0x00, 0x41, 0x87, 0x10, 0x20, 0x02, 0x6a, 0x20, 0x00, 0x20, 0x02, 0x6a, 0x2d, 0x00, 0x00, 0x3a, 0x00, 0x00, 0x20, 0x02, 0x41, 0x01, 0x6a, 0x21, 0x02, 0x0c, 0x01, 0x0b, 0x0b, 0x41, 0x80, 0x10, 0xad, 0x42, 0x20, 0x86, 0x41, 0x07, 0x20, 0x01, 0x6a, 0xad, 0x84, 0x0b, // Code Section
	}

	if err := os.WriteFile("examples/greet.wasm", wasmBinary, 0644); err != nil {
		log.Fatalf("failed to write wasm file: %v", err)
	}
	log.Println("Created examples/greet.wasm successfully")
}
